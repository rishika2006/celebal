/*
TOP 5 PROFITABLE CUSTOMERS
*/

SELECT
    Customer_ID,
    SUM(Profit) AS Total_Profit
FROM orders
GROUP BY Customer_ID
ORDER BY Total_Profit DESC
LIMIT 5;

-- Insight:
-- Identifies customers generating maximum profit.


/*
MOST PROFITABLE CATEGORY
*/

SELECT
    p.Category,
    SUM(o.Profit) AS Total_Profit
FROM orders o
JOIN products p
ON o.Product_ID = p.Product_ID
GROUP BY p.Category
ORDER BY Total_Profit DESC;

-- Insight:
-- Helps identify the category contributing the highest profit.


/*
REGION WISE SALES ANALYSIS
*/

SELECT
    c.Region,
    SUM(o.Sales) AS Region_Sales
FROM orders o
JOIN customers c
ON o.Customer_ID = c.Customer_ID
GROUP BY c.Region
ORDER BY Region_Sales DESC;

-- Insight:
-- Compares sales performance across regions.


/*
AVERAGE DISCOUNT BY CATEGORY
*/

SELECT
    p.Category,
    ROUND(AVG(o.Discount),2) AS Avg_Discount
FROM orders o
JOIN products p
ON o.Product_ID = p.Product_ID
GROUP BY p.Category;

-- Insight:
-- Shows which categories receive higher discounts.


/*
TOP SELLING PRODUCTS
*/

SELECT
    p.Product_Name,
    SUM(o.Quantity) AS Total_Quantity_Sold
FROM orders o
JOIN products p
ON o.Product_ID = p.Product_ID
GROUP BY p.Product_Name
ORDER BY Total_Quantity_Sold DESC
LIMIT 10;

-- Insight:
-- Identifies the most demanded products.


/*
CUSTOMERS GENERATING PROFIT WITH ABOVE AVERAGE DISCOUNT
*/

SELECT
    Customer_ID,
    SUM(Profit) AS Total_Profit,
    AVG(Discount) AS Avg_Discount
FROM orders
GROUP BY Customer_ID
HAVING AVG(Discount) >
(
    SELECT AVG(Discount)
    FROM orders
)
AND SUM(Profit) > 0;

-- Insight:
-- Finds customers who remain profitable despite receiving higher discounts.


/*
RANK PRODUCTS WITHIN EACH CATEGORY
*/

SELECT
    p.Category,
    p.Product_Name,
    SUM(o.Sales) AS Product_Sales,

    RANK() OVER
    (
        PARTITION BY p.Category
        ORDER BY SUM(o.Sales) DESC
    ) AS Category_Rank

FROM orders o
JOIN products p
ON o.Product_ID = p.Product_ID

GROUP BY
    p.Category,
    p.Product_Name;

-- Insight:
-- Ranks products against competitors within the same category.


