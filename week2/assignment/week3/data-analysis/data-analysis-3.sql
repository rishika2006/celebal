/*
SUBQUERY 1

Orders with sales greater than average sales
*/

SELECT *
FROM orders
WHERE Sales >
(
    SELECT AVG(Sales)
    FROM orders
);

-- Insight:
-- These orders have sales higher than the overall average sales value.


/*
SUBQUERY 2

Highest sales order for each customer
*/

SELECT *
FROM orders o
WHERE Sales =
(
    SELECT MAX(Sales)
    FROM orders
    WHERE Customer_ID = o.Customer_ID
);

-- Insight:
-- Finds the highest value order placed by each customer.


/*
CTE 1

Total sales generated by each customer
*/

WITH customer_sales AS
(
    SELECT
        Customer_ID,
        SUM(Sales) AS Total_Sales
    FROM orders
    GROUP BY Customer_ID
)

SELECT *
FROM customer_sales
ORDER BY Total_Sales DESC;

-- Insight:
-- Helps identify customers contributing the most revenue.


/*
CTE 2

Total profit generated by each customer
*/

WITH customer_profit AS
(
    SELECT
        Customer_ID,
        SUM(Profit) AS Total_Profit
    FROM orders
    GROUP BY Customer_ID
)

SELECT *
FROM customer_profit
ORDER BY Total_Profit DESC;

-- Insight:
-- Shows which customers are the most profitable.


/*
WINDOW FUNCTION

ROW_NUMBER
*/

SELECT
    Customer_ID,
    Sales,

    ROW_NUMBER() OVER
    (
        PARTITION BY Customer_ID
        ORDER BY Sales DESC
    ) AS Order_Number

FROM orders;

-- Insight:
-- Assigns a unique sequence number to each order per customer.


/*
WINDOW FUNCTION

RANK
*/

SELECT
    Customer_ID,
    SUM(Sales) AS Total_Sales,

    RANK() OVER
    (
        ORDER BY SUM(Sales) DESC
    ) AS Customer_Rank

FROM orders
GROUP BY Customer_ID;

-- Insight:
-- Ranks customers based on their total sales.


/*
WINDOW FUNCTION

DENSE_RANK
*/

SELECT
    Customer_ID,
    SUM(Sales) AS Total_Sales,

    DENSE_RANK() OVER
    (
        ORDER BY SUM(Sales) DESC
    ) AS Dense_Rank_Value

FROM orders
GROUP BY Customer_ID;

-- Insight:
-- Similar to rank but without skipping rank numbers.


/*
FINAL TASK

JOIN + CTE + WINDOW FUNCTION
*/

WITH customer_sales AS
(
    SELECT
        Customer_ID,
        SUM(Sales) AS Total_Sales
    FROM orders
    GROUP BY Customer_ID
)

SELECT
    c.`Customer ID`,
    c.`Customer Name`,
    cs.Total_Sales,

    RANK() OVER
    (
        ORDER BY cs.Total_Sales DESC
    ) AS Sales_Rank

FROM customers c
JOIN customer_sales cs
ON c.`Customer ID` = cs.Customer_ID

ORDER BY Sales_Rank;

-- Insight:
-- Combines joins, CTEs and window functions to rank customers by sales.


